    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        
        // Ajustamos el formato de fecha para poder realizar la consulta (a√±o-mes-dia)
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        
        String fi = sdf.format(txtdcFecIni.getDate());
        String ff = sdf.format(txtdcFecFin.getDate());
        
        // Definimos un nombre para las columnas de nuestra tabla a consultar
        String[] columnNombre = {"Fec. Inicio", "Ciudad", "Constructora", "Nom. Lider", "Cod. Tipo", "Estrato"};
        
        ControllerRequerimientos controlador = new ControllerRequerimientos();
        
        ArrayList<Requerimiento_2> listaR;
        
        try {
            // Enviamos la consulta con los parametros ingresados por el usuario utilizando campos texto para las fechas
            // listaR = controlador.consultaRequerimiento2(txtFecIni.getText(), txtFecFin.getText(), txtCiudad.getText());
            
            // Enviamos la consulta con los parametros ingresados por el usuario utilizando el selector de calendario para las fechas
            listaR = controlador.consultaRequerimiento2(fi, ff, txtCiudad.getText());
            
            Object[][] datos = new Object[listaR.size()][6];
            
            int index = 0;
            for(Requerimiento_2 r: listaR){
                datos[index][0] = r.getFecIni();
                datos[index][1] = r.getNombreCiudad();
                datos[index][2] = r.getNomConstructora();
                datos[index][3] = r.getNombreLider();
                datos[index][4] = r.getCodigoTipo();
                datos[index][5] = r.getEstrato();
                
                index++;
            }
            
            DefaultTableModel model = new DefaultTableModel(datos,columnNombre);
            
            miTablaBusqueda.setModel(model);
            
        } catch (SQLException e){
            System.err.println(e);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

